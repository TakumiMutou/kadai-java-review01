
public class ミニ課題 {

    public static void main(String[] args) {
        //変数の宣言・初期化
        int num1 = 100; // int型変数ｎｕｍ１を宣言し、100を代入
        int num2 = 30; // int型変数num2を宣言し、10を代入

        //int型変数resultを宣言し、ｎｕｍ１とｎｕｍ２の加算結果を代入
        int result = num1 + num2;
        //加算結果を出力
        System.out.println("num1 + num2 = " + result);
        //減算結果
        result = num1 - num2;
        System.out.println("num1 - num2 = " + result);
        //乗算結果
        result = num1 * num2;
        System.out.println("num1 * num2 = " + result);
        //除算結果
        result = num1 / num2;
        System.out.println("num1 / num2 = " + result);
        //剰余算結果
        result = num1 % num2;
        System.out.println("num1 % num2 = " + result);


    }

}
